    void insert(String xTower, int xSound, int xType) {

        if (xTower.charAt(0) == 'B') {
            return;
        }

        Bell newBell = new Bell(xTower, xSound, xType);
        root = insertRec(root, newBell);
    }

    private Node insertRec(Node root, Bell newBell) {
        if (root == null) {
            return new Node(newBell);
        }

        if (newBell.sound < root.info.sound) {
            root.left = insertRec(root.left, newBell);
        } else if (newBell.sound > root.info.sound) {
            root.right = insertRec(root.right, newBell);
        }
        return root;
    }

    void postOrder2(Node p, RandomAccessFile f) throws Exception {
        if (p == null) {
            return;
        }

        postOrder2(p.left, f);
        postOrder2(p.right, f);
        if (p.info.type > 4) {
            fvisit(p, f);
        }
    }

    public int childrenCount(Node root) {
        if (root == null) {
            return 0;
        }
        return 1 + childrenCount(root.left) + childrenCount(root.right);
    }

    public Node find4th(Node root) {
        if (root == null) {
            return null;
        }

        int count = 0;
        Node node = root;
        Stack<Node> stack = new Stack<>();
        while (node != null || !stack.isEmpty()) {
            while (node != null) {
                stack.push(node);
                node = node.left;
            }

            node = stack.pop();
            count++;
            if (count == 4) {
                return node;
            }

            node = node.right;
        }

        return null;
    }

